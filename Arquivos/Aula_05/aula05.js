// Aula 05 - Operadores Relacionais em JavaScript

// Maior que (>)
console.log(10 > 5); // true

// Maior ou igual (>=)
console.log(10 >= 10); // true

// Menor que (<)
console.log(5 < 10); // true

// Menor ou igual (<=)
console.log(5 <= 5); // true

// Igualdade (==)
console.log(5 == '5'); // true (compara apenas o valor)

// Diferente (!=)
console.log(5 != 10); // true

// Estritamente igual (===)
console.log(5 === '5'); // false (compara valor e tipo)

// Estritamente diferente (!==)
console.log(5 !== '5'); // true (compara valor e tipo)

// NOT (!)
console.log(!(10 > 5)); // false (negação do resultado)