// Declarando a variável n1 com valor 10
let n1 = 10

// Declarando a variável n2 com valor 20
let n2 = 20

// Aqui estamos usando o operador + para concatenar os valores
// Como há uma string (" ") entre n1 e n2, o JavaScript converte os números em strings
// Resultado: "10 20"
console.log(n1 + " " + n2) // Saída: 10 20
