// Declarando a variável n com valor inicial 18
let n = 18;

// Pré-incremento: o valor de n é incrementado ANTES de ser usado
// Ou seja, n passa a valer 19 e esse valor é impresso no console
console.log(++n) // Saída: 19

// Declarando a variável m com valor inicial 20
let m = 20

// Pós-incremento: o valor de m é usado ANTES de ser incrementado
// Ou seja, imprime 20 no console, e só depois m passa a valer 21
console.log(m++) // Saída: 20

// Agora m já foi incrementado, então seu novo valor (21) é impresso
console.log(m) // Saída: 21
// Saída final: 19, 20, 21