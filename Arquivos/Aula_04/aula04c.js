// Declarando e inicializando as variáveis
let num1 = 0, num2 = 0, num3 = 0;

// Atribuição simples
num1 = 5
num2 = 10

// Operador de soma (+): soma num1 e num2
num3 = num1 + num2
console.log(num3) // Exibe 15

// Operador de subtração (-): subtrai num1 de num2
num3 = num2 - num1
console.log(num3) // Exibe 5

// Parênteses alteram a ordem de precedência dos operadores
// Soma num1 e num2, depois multiplica o resultado por 2
num3 = (num1 + num2) * 2
console.log(num3) // Exibe 30

// Operador de divisão (/): divide num1 por num2
num3 = 0
num1 = 5
num2 = 10
num3 = num1 / num2
console.log(num3) // Exibe 0.5

// Operador de resto (%) ou módulo: retorna o resto da divisão de num2 por num1
num3 = 0
num1 = 3
num2 = 10
num3 = num2 % num1
console.log(num3) // Exibe 1 (resto da divisão de 10 por 3)

// Operador de incremento (++): aumenta o valor da variável em 1
num1++
console.log(num1) // Exibe 6 (num1 era 5, agora é 6)

// Operador de decremento (--): diminui o valor da variável em 1
num2--
console.log(num2) // Exibe 9 (num2 era 10, agora é 9)

// Operador de atribuição composta (+=): soma 1 ao valor atual de num1
num1 += 1
console.log(num1) // Exibe 7 (num1 era 6, agora é 7)

// Operador de atribuição composta (-=): subtrai 2 do valor atual de num1
num1 -= 2
console.log(num1) // Exibe 5 (num1 era 7, agora é 5)