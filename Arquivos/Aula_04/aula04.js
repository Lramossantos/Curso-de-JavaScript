// Exemplos práticos de Operadores Matemáticos em JavaScript

// Soma (+)
let a = 5;
let b = 3;
let soma = a + b; // 8
console.log("Soma:", soma);

// Subtração (-)
let subtracao = a - b; // 2
console.log("Subtração:", subtracao);

// Multiplicação (*)
let multiplicacao = a * b; // 15
console.log("Multiplicação:", multiplicacao);

// Divisão (/)
let divisao = a / b; // 1.666...
console.log("Divisão:", divisao);

// Resto da divisão (%)
let resto = a % b; // 2
console.log("Resto da divisão:", resto);

// Exponenciação (**)
let potencia = a ** b; // 125
console.log("Exponenciação:", potencia);

// Incremento (++):
let x = 1;
x++;
console.log("Incremento:", x); // 2

// Decremento (--):
x--;
console.log("Decremento:", x); // 1
